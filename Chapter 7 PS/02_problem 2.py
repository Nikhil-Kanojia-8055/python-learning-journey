# WAP for greeting persons whose name is starting with N 

l = ["Nk","Rahul","Manoj","Nikhil"]
for name in l:
    if(name.startswith("N")):
        print(f"Hello {name}")

# ending with l

l = ["Nk","Rahul","Manoj","Nikhil"]
for name in l:
    if(name.endswith("l")):
        print(f"Hello {name}")
# using of break statement

for i in range(1,40):
    if(i==20):
            break 
# exit the loop right now


    print(i)

# using of continue statement

for i in range(1,40):
    if(i==20):
            continue
# skip this iteration


    print(i)

for i in range(5):
    print("hello")
    if(i==3):
        continue
    print(i)
  